// String
let firstName = "mike";
let lastName = "gioia";

// Number
let myAge = 38;
let myHeight = 5.7;

// Array
let favorites = ['Soccer', 'Football'];

// Boolean
let isTrue = true;
let isFalse = false;

// Object
let person = {
  name: firstName + ' ' + lastName,
  age: myAge
};

console.log(firstName + ' ' + lastName);
console.log(myAge, myHeight);
console.log(favorites);
console.log(isTrue, isFalse, !isTrue, !isFalse);
console.log(person.name);
